import java.util.*;
public class StringConcatenation {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
        System.out.println("first string");
		String str1 = sc.next();
		System.out.println("Second string");
        String str2 = sc.next();
        System.out.println(str1 + " " + str2);
	}

}
